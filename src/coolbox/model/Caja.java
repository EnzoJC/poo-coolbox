package coolbox.model;

/**
 *
 * @author alexa
 */
public class Caja {
    private int id;
    private float monto;

    public Caja(int id, float monto) {
        this.id = id;
        this.monto = monto;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public float getMonto() {
        return monto;
    }

    public void setMonto(float monto) {
        this.monto = monto;
    }
    
}
